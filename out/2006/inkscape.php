    <h2>
     Inkscape
    </h2>
    <img alt="Inkscape Logo" src="images/inkscape-logo.png"/>
    <p>
     Inkscape is the leading free software vector graphics package. It has a
  vibrant developer and user community. It's available for GNU/Linux, 
  Windows, MacOS X and all flavours of Unix.
    </p>
   </div>
   <hr/>
   <p>
    Inkscape is a vector drawing program with capabilities similar to
Illustrator, Freehand, and CorelDraw. It uses the W3C standard SVG as
its native document format.
   </p>
   <p>
    The Inkscape project has a stormy past, coming as it
does from a fork with another project, Sodipodi. The advances made in
Inkscape in a relatively short period have set it apart from its
predecessor, and Inkscape is now the premier free software vector art
program, with support for features like flowed text, styles and
tileable cloning.
   </p>
   <p>
    Perhaps the most impressive feature of Inkscape is the
way that the project has gathered a large group of passionnate users
and developers, working together to make a high quality drawing program
for artists, illustrators, designers and hobbyists. Inkscape is vector
drawing for real people.
   </p>
