   <hr/>
   <p>
    Since its humble beginning in the spring of 2001,
Scribus has rapidly developed into one of the premier free software 
desktop applications. Called by Newsforge, "..one of the killer
applications for Linux", Scribus brings new power and versatile tools
for page layout. New features like separation previews, cross-platform
python scripting, advanced PDF 1.4 support are just some of the
hundreds of improvements large and small in Scribus 1.2.x. Scribus
1.3.x development series is underway with more features and
enhancements.
   </p>
   <p>
    Underneath the modern and user friendly interface,
Scribus supports professional publishing features, such as CMYK color,
separations, ICC color management and versatile PDF creation. Scribus
was the first DTP application on the planet to directly support PDF/X-3
output, a rigorous ISO standard. Scribus did so by almost a year. Other
features include PDF Import, EPS import/export, Unicode text including
right to left scripts such as Arabic and Hebrew.
